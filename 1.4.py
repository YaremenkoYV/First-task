a = int(input('Введіть ділене: '))
b = int(input('Введіть дільник: '))
res = 0
if b == 0:
    print("Помилка ділити на 0 не можна.")
elif a < b:
    print("Перезапустіть програму та введіть такі числа, щоб ділене було більше дільнику")
else:
    while a >= b:
        res += 1
        a -= b
    print("Частка = ", res)
    print("Остача = ", a)
